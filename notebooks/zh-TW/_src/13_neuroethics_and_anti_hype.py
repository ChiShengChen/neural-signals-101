# # 演示——執行前：先思考
#
# 我們即將生成**純隨機噪音**作為「EEG 特徵」，並分配**完全隨機的二元標籤**。
# 從構造上看，特徵和標籤之間的關係為零。
#
# **執行前：預測每種方法會報告什麼。**
#
# - **WRONG（錯誤）** 方法使用*全部*資料（有洩漏）選取 30 個「最佳」特徵，然後進行交叉驗證。
#   你預期準確率是多少？
# - **RIGHT（正確）** 方法將特徵選取放入流水線（pipeline）中，
#   因此選取只在每個折疊中看到訓練資料。你預期準確率是多少？
#
# 在這裡寫下你的預測（或只是在心中想一下）：
# - 有洩漏的方法：____%
